function 합격했냐(a, b) {
  // 0에서 100 사이의 숫자가 아닌 경우 "장난금지" 메시지 출력
  if (a < 0 || a > 100 || b < 0 || b > 100) {
    console.log("장난금지");
    return; // 함수 종료
  }

  // 합격 여부 판단
  if ((a < 40 || b < 40) || a + b < 120) {
    console.log("불합격입니다");
  } else if (a + b >= 120) {
    console.log("합격입니다");
  }
}

// 예시 사용
합격했냐(10101, 100); // "장난금지" 출력
합격했냐(50, 70); // "합격입니다" 출력
합격했냐(30, 50); // "불합격입니다" 출력
합격했냐(110, 90); // "장난금지" 출력
